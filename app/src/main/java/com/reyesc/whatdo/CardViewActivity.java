package com.reyesc.whatdo;

public class CardViewActivity {
    private int id;
    private int image;
    private String date;
    private String title;
    private String tag;
    private String description;

    public CardViewActivity(int id, int image, String date, String title, String tag, String description) {
        this.id = id;
        this.image = image;
        this.title = date;
        this.title = title;
        this.tag = tag;
        this.description = description;
    }
}
